public class Perbandingan {
    public static void main(String[] args) {

        int Value1 = 100;
        int Value2 = 150;

        System.out.println(Value1 > Value2);
        System.out.println(Value1 < Value2);
        System.out.println(Value1 >= Value2);
        System.out.println(Value1 <= Value2);
        System.out.println(Value1 == Value2);
        System.out.println(Value1 != Value2);

    }
}
